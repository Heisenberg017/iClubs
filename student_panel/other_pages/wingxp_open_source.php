        <div class="page-container-max">
            <div class="row">
                <div class="col-xs-12 col-sm-12 col-md-4 col-lg-4 col-xl-4" style="margin-bottom: 20px">
                    <h1 class="detail-wrap-head">Operating Systems</h1>
                    <ul class="detail-wrap-list">
                        <li><a href="#">Linux Mint</a></li>
                    </ul>
                    <p class="detail-wrap-para-info">For those who liked the feel of Windows XP, this system is designed
                        to be helpful to those who
                        don't want to try to hard to learn complex Linux commands (though it provides access to this as
                        well).</p>
                    <p class="detail-wrap-para-info no-margin">
                        Tired of being locked into systems created by Microsoft or Apple? The following systems are Open
                        Source. Though these have traditionally been thought of as harder to use and "only for experts",
                        this has changed considerably, and many Linux distributions are ready for average users. There
                        are many more than these, but here is a quick sampling.
                    </p>
                    <ul class="detail-wrap-list">
                        <li><a href="#">ReactOS</a></li>
                    </ul>
                    <p class="detail-wrap-para-info">Designed to be a an easy to use replacement for Windows, ReactOS is
                        designed to be able to run software from multiple platforms well.</p>
                    <ul class="detail-wrap-list">
                        <li><a href="#">Ubuntu</a></li>
                    </ul>
                    <p class="detail-wrap-para-info">Ubuntu is the most popular and one of the easiest to use Linux
                        distributions. You can install it on a PC, Laptop, or Phone</p>
                </div>
                <div class="col-xs-12 col-sm-12 col-md-4 col-lg-4 col-xl-4" style="margin-bottom: 20px">
                    <h1 class="detail-wrap-head">Learning Management Systems</h1>
                    <ul class="detail-wrap-list">
                        <li><a href="#">Moodle</a></li>
                    </ul>
                    <p class="detail-wrap-para-info">A free, open-source PHP web application for producing modular
                        internet-based courses</p>
                    <p class="detail-wrap-para-info no-margin">This software is designed to provide alternatives to
                        large
                        educational course software, also
                        known as Learning Management Systems. Many find them easier to use than some of the larger
                        commercial products.</p>
                    <ul class="detail-wrap-list">
                        <li><a href="#">.LRN</a></li>
                    </ul>
                    <p class="detail-wrap-para-info">Enterprise-class open source software for supporting e-learning and
                        digital communities.</p>
                    <ul class="detail-wrap-list">
                        <li><a href="#">Dokeos</a></li>
                    </ul>
                    <p class="detail-wrap-para-info">There are multiple versions, the first of which is free and open
                        source. Among other features, it includes the ability to convert Office documents into "learning
                        paths."</p>
                    <ul class="detail-wrap-list">
                        <li><a href="#">Sakai</a></li>
                    </ul>
                    <p class="detail-wrap-para-info">This open source learning management system was built by MIT,
                        Stanford, and Berkeley. It was designed by academics for academics.</p>
                </div>
                <div class="col-xs-12 col-sm-12 col-md-4 col-lg-4 col-xl-4">
                    <h1 class="detail-wrap-head">Open Source Software</h1>
                    <ul class="detail-wrap-list">
                        <li><a href="#">Open Office</a></li>
                    </ul>
                    <p class="detail-wrap-para-info">Can't afford the full Microsoft Office Suite? Open Office is a free
                        alternative that includes a Word Processor, Spreadsheets, Presentation, and Database Software.
                        All files can be saved in common formats, so they are easy to share.</p>
                    <p class="detail-wrap-para-info no-margin">Sometimes there is basic software that students and
                        faculty need, but the cost can be somewhat
                        prohibitive. However there are many free open source alternatives available, that will suffice
                        (and sometimes are better at certain things than their closed-source commercial alternatives).
                        They will usually run in most major operating systems. Here are a few useful ones.
                    </p>
                    <ul class="detail-wrap-list">
                        <li><a href="#">GIMP (GNU Image Manipulation Program)</a></li>
                    </ul>
                    <p class="detail-wrap-para-info">This is an excellent Open Source alternative image manipulation
                        software. It can perform almost anything that can be done in Photoshop, and it has a very large
                        community so there are many free tutorials online</p>
                    <ul class="detail-wrap-list">
                        <li><a href="#">Inkscape</a></li>
                    </ul>
                    <p class="detail-wrap-para-info">A free Open Source alternative to Adobe Illustrator</p>
                    <ul class="detail-wrap-list">
                        <li><a href="#">Audacity</a></li>
                    </ul>
                    <p class="detail-wrap-para-info">Free open source recording and sound editing software.</p>
                    <ul class="detail-wrap-list">
                        <li><a href="#">Jing</a></li>
                    </ul>
                    <p class="detail-wrap-para-info">This is the free and easy-to-use version of Camtasia's screencast
                        software (not open source, but it is free). It is good for creating short tutorials or
                        communicating a concept with a brief visual demonstration.</p>
                </div>
